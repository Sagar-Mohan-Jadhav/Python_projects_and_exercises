# 'and' operation gives second value if bool(first value) is true and gives first value if bool(first value) is false
print(4 and 0)
print(0 and 'car')
print('' and '')
print('car' and 'far')


# 'or' operation gives first value if bool(first value) is true and gives second value if bool(first value) is false
print('' or 4)
print(0 or 4)
print(0 or '')
print(4 or 5)
